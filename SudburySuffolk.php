<div class="section2" style="height:240px;">
    <h3>The town</h3>
    <p>The market place is the centre of the town with many fine buildings 
    overseen by St Peters Church, now used for many community purposes.</p>
    <p>There are many fine restaurants, cafes and bars in town as well as banks 
    and a mix of shops.</p>
    <p>There is easy access to the town, with plenty of parking, up to 3 
    hours free.</p>
