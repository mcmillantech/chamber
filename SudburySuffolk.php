<div class="section2" style="height:240px;">
    <h3>Attractions</h3>
    <p>Sudbury is the birthpace of Gainsborough. <a href="http://www.gainsborough.org/">
    Gainborough's House</a>, his 
    birthplace is a major gallery (currently closed for a major expansion).</p>
    <p>Belle Vue Park is well worth a visit, with tennis courts, playgrounds
    and a skateboard park. The Kingfisher Leisure Centre provides a swimming 
    pool and other sporting facilities.</p>
    <p>400 metres along Friars Street is Quay Lane, which leads to the 
    <a href="https://www.quaysudbury.com/">Quay Theatre</a> and the River 
    Stour Trust. In Summer weekends, there are boat 
    trips and a tea room here.</p>
</div>

<div style="clear: both"><br> </div>

<div class="section2">
    <h3>Countryside</h3>
    <p>The Sudbury water meadows are beautiful. They are crossed by many 
    footpaths and are excellent for walking. Friars Meadow, closest to the town, 
    is an ideal place for a picnic.</p>
    <p>Next to the Kingfisher Leisure Centre, The Valley Trail starts. This runs 
    along a disused railway as far as Long Melford. The Trail forms part of the 
    National Cycle Network Route 13 and the St Edmunds Way.</p>
</div>

<div class="section2">
    <h3>Business in Sudbury</h3>
